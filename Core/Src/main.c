/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

	// Enable DWT for microsecond timing
	CoreDebug->DEMCR |= CoreDebug_DEMCR_TRCENA_Msk;
	DWT->CYCCNT = 0;
	DWT->CTRL |= DWT_CTRL_CYCCNTENA_Msk;


  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* Configure the peripherals common clocks */
  PeriphCommonClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  MX_ADC2_Init();
  MX_ADC3_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */

  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);

  //reset the motors to 0 on startup
	__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, 600);
	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, 600);


  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */



	  	//start analog to digital control for mics
	    uint32_t adc_val1, adc_val2, adc_val3;
	    uint32_t now = HAL_GetTick();
	    // --- ADC1 ---
	    HAL_ADC_Start(&hadc1);
	    // --- ADC2 ---
	    HAL_ADC_Start(&hadc2);
	    // --- ADC3 ---
	    HAL_ADC_Start(&hadc3);

	    // Clear LEDs every .5 seconds
	    // reset trigger timer so mics don't sample while the motors are turning
	    static uint32_t last_trigger = 0;
	    if (now - last_trigger >= 500) {
	        HAL_GPIO_WritePin(LED1_GPIO_Port, LED1_Pin, 0);
	        HAL_GPIO_WritePin(LED2_GPIO_Port, LED2_Pin, 0);
	        HAL_GPIO_WritePin(LED3_GPIO_Port, LED3_Pin, 0);
	        last_trigger = 0;
	    }

	    //Sample all three mics as fast as the while loop can go
	    //When mic1 hears a loud sound, record the time in milliseconds
	    if (HAL_ADC_PollForConversion(&hadc1, 1) == HAL_OK && !mic1Time && !last_trigger) {
	        adc_val1 = HAL_ADC_GetValue(&hadc1);
	        if (adc_val1 <= 500 || adc_val1 >= 2500) {
	            mic1Time = getMicroTime();
	        }
	    }

	    //When mic2 hears a loud sound, record the time in milliseconds
	    if (HAL_ADC_PollForConversion(&hadc2, 1) == HAL_OK && !mic2Time && !last_trigger) {
	        adc_val2 = HAL_ADC_GetValue(&hadc2);
	        if (adc_val2 <= 500 || adc_val2 >= 2500) {
	            mic2Time = getMicroTime();
	        }
	    }

	    //When mic3 hears a loud sound, record the time in milliseconds
	    if (HAL_ADC_PollForConversion(&hadc3, 1) == HAL_OK && !mic3Time && !last_trigger) {
	        adc_val3 = HAL_ADC_GetValue(&hadc3);
	        if (adc_val3 <= 500 || adc_val3 >= 2500) {
	            mic3Time = getMicroTime();
	        }
	    }

	    // if all mics have heard a loud sound:
	    //use the time differences to calculate the angle the sound came from,
	    //pause sampling and indicate the pause with an led
	    //point the servo motors in the direction of the sound using pwm
	    if (mic1Time && mic2Time && mic3Time){

	    	//turn on a light to indicate which mic heard the sound first.
	    	if (mic1Time <= mic2Time && mic1Time <= mic3Time){
	    		HAL_GPIO_WritePin(LED1_GPIO_Port, LED1_Pin, 1);
	    	}
	    	else if (mic2Time <= mic1Time && mic2Time <= mic3Time){
	    		HAL_GPIO_WritePin(LED2_GPIO_Port, LED2_Pin, 1);
	    	}
	    	else if (mic3Time <= mic2Time && mic3Time <= mic1Time){
	    		HAL_GPIO_WritePin(LED3_GPIO_Port, LED3_Pin, 1);
	    	}

	    	//convert micro seconds into seconds for the calculation
	    	float mic1Seconds = mic1Time / 1e6f;
	    	float mic2Seconds = mic2Time / 1e6f;
	    	float mic3Seconds = mic3Time / 1e6f;

	    	//determine weights for the weighted average based on when each mic heard the sound
	    	float w1 = 1.0f / (mic1Seconds + 1e-6f);
	    	float w2 = 1.0f / (mic2Seconds + 1e-6f);
	    	float w3 = 1.0f / (mic3Seconds + 1e-6f);

	    	//mic1 is located at 0 degrees. Use this to determine where it is relative to the others
	    	float mic1x = cosf(0.0f * M_PI / 180.0f);
	    	float mic1y = sinf(0.0f * M_PI / 180.0f);

	    	//mic2 is located at 120 degrees. Use this to determine where it is relative to the others
	    	float mic2x = cosf(120.0f * M_PI / 180.0f);
	    	float mic2y = sinf(120.0f * M_PI / 180.0f);

	    	//mic3 is located at 240 degrees. Use this to determine where it is relative to the others
	    	float mic3x = cosf(240.0f * M_PI / 180.0f);
	    	float mic3y = sinf(240.0f * M_PI / 180.0f);

	    	//create a vector pointed in the direction of the sound based off the weighted averages
	    	float dx = (w1 * mic1x + w2 * mic2x + w3 * mic3x);
	    	float dy = (w1 * mic1y + w2 * mic2y + w3 * mic3y);

	    	//use inverse tangent to calculate the angle of the vector (the direction of the sound)
	    	float angle_rad = atan2f(dy, dx);
	    	//convert to degrees
	    	float angle_deg = angle_rad * 180.0f / M_PI;
	    	if (angle_deg < 0) angle_deg += 360.0f;

	    	//since the servo motors can only turn 180 degrees, we need a stack of two to achieve 360 range.
	    	//turn the first motor to the calculated angle if the angle is less than 180
	    	if (angle_deg < 180){
	    		uint16_t pulse = 600 + (angle_deg * 2250) / 180; //map the angle to a valid pwm signal
	    		__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, 600); //set the second motor to angle 0
	    		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, pulse); //set the first motor to the calculated angle
	    	}
	    	else{
	    		//if the angle is greater than 180, turn the first motor all the way, and then make up the difference with the second motor
	    		uint16_t pulse = 600 + ((angle_deg - 180) * 2250) / 180; //map the angle to a valid pwm signal
	    		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, 2850); //turn the first motor all the way
	    		__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, pulse); //set the second motor to the calculated angle
	    	}

	    	//reset variables so that the mics can start sampling again after the timer is done
	    	last_trigger = now;
	    	mic1Time = 0;
	    	mic2Time = 0;
	    	mic3Time = 0;
	    }
  }
  /* USER CODE END 3 */
}

